package tecnico.atividade.presencial;

public class Jogos {
    private String melhorJogo;
    private int quantidadeJogos;
    private String generoMelhorJogo;

    public String getMelhorJogo() {
        return melhorJogo;
    }

    public void setMelhorJogo(String melhorJogo) {
        this.melhorJogo = melhorJogo;
    }

    public int getQuantidadeJogos() {
        return quantidadeJogos;
    }

    public void setQuantidadeJogos(int quantidadeJogos) {
        this.quantidadeJogos = quantidadeJogos;
    }

    public String getGeneroMelhorJogo() {
        return generoMelhorJogo;
    }

    public void setGeneroMelhorJogo(String generoMelhorJogo) {
        this.generoMelhorJogo = generoMelhorJogo;
    }
    
    
}
