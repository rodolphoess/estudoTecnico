package tecnico.atividade.presencial;

public class Contatos {
    private String contatoPrincipal;
    private int quantidadeContatos;
    private int contatosIndispensaveis;

    public String getContatoPrincipal() {
        return contatoPrincipal;
    }

    public void setContatoPrincipal(String contatoPrincipal) {
        this.contatoPrincipal = contatoPrincipal;
    }

    public int getQuantidadeContatos() {
        return quantidadeContatos;
    }

    public void setQuantidadeContatos(int quantidadeContatos) {
        this.quantidadeContatos = quantidadeContatos;
    }

    public int getContatosIndispensaveis() {
        return contatosIndispensaveis;
    }

    public void setContatosIndispensaveis(int contatosIndispensaveis) {
        this.contatosIndispensaveis = contatosIndispensaveis;
    }
    
    
}
